public class IncrementOp {
    public static void main (String[] args){

        int a = 10;
        System.out.println("Value of a: " + a);

        //a = a + 1;
        a++;
        System.out.println("Incremented value of a: " + a);

        System.out.println("=========================================");

        a+=2;
        System.out.println("Incremented value of a by 2: " + a);

        // HW a--; a-=5;

        System.out.println("==========================================");

        a = 10;
        a*= 10;
        System.out.println("Multiplied a: " + a);

    }//psvm
}//class
